package KonsepPerulangan05;

public class perulanganFor {
    public static void main(String[] args) {

        for(int i=10; i>=0; i--) {
            System.out.print(i + " ");
        }
        System.out.println();

        for(int i=0; i<=5; i++) {
            System.out.print(i + " ");
        }
        System.out.println();

        for(int i=0; i<=5; i+=2) {
            System.out.print(i + " ");
        }

    }
}
